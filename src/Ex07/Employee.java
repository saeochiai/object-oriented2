package Ex07;

public class Employee {

        private String name;  //従業員名
        private int salary;   //給料額


        //getter/setterを作成
        public String getName() {
            return name;
        }
        public void setName(String name) {
            this.name = name;
        }
        public int getSalary() {
            return salary;
        }
        public void setSalary(int salary) {
            this.salary = salary;
        }






    



}

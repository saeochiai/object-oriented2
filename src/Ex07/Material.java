package Ex07;

public class Material {

    private String name; //材料名
    private int cost; //コスト（原価）



    //getter/setterを作成
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getCost() {
        return cost;
    }
    public void setCost(int cost) {
        this.cost = cost;
    }

    


}
